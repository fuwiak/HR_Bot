    log.info(f"üöÄ –ù–∞—á–∏–Ω–∞–µ–º –º–∏–≥—Ä–∞—Ü–∏—é {len(services)} —É—Å–ª—É–≥ –≤ RAG –∫–æ–ª–ª–µ–∫—Ü–∏—é...")
    
