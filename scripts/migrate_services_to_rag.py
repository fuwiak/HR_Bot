        log.info(f"‚úÖ –£—Å–ø–µ—à–Ω–æ –∑–∞–≥—Ä—É–∂–µ–Ω–æ {successful} —É—Å–ª—É–≥ –≤ RAG –∫–æ–ª–ª–µ–∫—Ü–∏—é '{COLLECTION_NAME}'")
